package F_commandpattern.commands.Light;

import F_commandpattern.commands.Command;
import F_commandpattern.receivers.Light;

public class LightOnCommand implements Command {

    Light light;

    public LightOnCommand(Light light) {
        this.light = light;
    }

    @Override
    public void execute() {
        light.on();
    }

    @Override
    public void undo() {
        light.off();
    }
}
