package M_compoundPattern.b_goose_adapter;

import M_compoundPattern.a_quackable_strategy.Quackable;

public class GooseAdapter implements Quackable {

    private Goose goose;

    public GooseAdapter(Goose goose) {
        this.goose = goose;
    }

    @Override
    public void quack() {
        goose.honk();
    }
}
